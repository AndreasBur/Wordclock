/*! \brief          
 *  \details        
 *                  
 *  \return         -
******************************************************************************************************************************************************/
stdReturnType AnimationTeletype::setClock(byte Hour, byte Minute)
{
    stdReturnType ReturnValue{E_OK};

    if(pClock->getClockWords(Hour, Minute, ClockWordsTable) == E_NOT_OK) ReturnValue = E_NOT_OK;
    CurrentWordIndex = 0;
    CurrentCharIndex = 0;
    CurrentWordLength = pDisplay->getWordLengthFast(ClockWordsTable[CurrentWordIndex]);
    State = STATE_WORKING;

    return ReturnValue;
} /* setClock */


