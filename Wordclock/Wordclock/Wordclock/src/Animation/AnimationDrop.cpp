/******************************************************************************************************************************************************
  getNextActivePixel()
******************************************************************************************************************************************************/
/*! \brief
 *  \details
 *
 *  \return         -
******************************************************************************************************************************************************/
stdReturnType AnimationDrop::setNextActivePixelIndex()
{
    for(int16_t Index = DISPLAY_NUMBER_OF_PIXELS - 1; Index >= 0; Index--) {
        if(pDisplay->getPixelFast(Index)) {
            CurrentPixelIndex = Index; 
            return E_OK;
        }
    }
    return E_NOT_OK;
}

