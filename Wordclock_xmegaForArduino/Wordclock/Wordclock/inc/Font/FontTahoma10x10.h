/* FontTahoma10x10 configuration parameter */


/* FontTahoma10x10 parameter */
#define FONT_TAHOMA_10X10_WIDTH                    10u
#define FONT_TAHOMA_10X10_HEIGHT                   10u
#define FONT_TAHOMA_10X10_FONT_TABLE_SIZE          102u


/******************************************************************************************************************************************************
 *  G L O B A L   F U N C T I O N   M A C R O S
******************************************************************************************************************************************************/


/******************************************************************************************************************************************************
 *  C L A S S   F O N T T A H O M A 1 0 X 1 0
******************************************************************************************************************************************************/
class FontTahoma10x10 : public Font<FontCharHorizontal<uint16_t, FONT_TAHOMA_10X10_HEIGHT>, FONT_TAHOMA_10X10_FONT_TABLE_SIZE>
{
/******************************************************************************************************************************************************
 *  P U B L I C   D A T A   T Y P E S   A N D   S T R U C T U R E S
******************************************************************************************************************************************************/
  public:
    using FontCharType = FontCharHorizontal<uint16_t, FONT_TAHOMA_10X10_HEIGHT>;
    using RowsType = std::array<uint16_t, FONT_TAHOMA_10X10_HEIGHT>;
  
/******************************************************************************************************************************************************
 *  P R I V A T E   D A T A   A N D   F U N C T I N O N S
******************************************************************************************************************************************************/
  private:
    static const FontTableType FontTable;

/******************************************************************************************************************************************************
 *  P U B L I C   F U N C T I O N S
******************************************************************************************************************************************************/
  public:
    constexpr FontTahoma10x10() : Font(FontTable) {
        
    }
    
    ~FontTahoma10x10() {
        
    }

    // get methods
    byte getWidth() const { return FONT_TAHOMA_10X10_WIDTH; }
    byte getHeight() const { return FONT_TAHOMA_10X10_HEIGHT; }
    Orientation getOrientation() const { return Orientation::ORIENTATION_HORIZONTAL; }
    const FontTableType& getFontTable() const { return FontTable; }

    // set methods

    // methods

};


