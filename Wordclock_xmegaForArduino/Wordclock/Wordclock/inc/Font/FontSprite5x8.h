/* FontSprite5x8 configuration parameter */


/* FontSprite5x8 parameter */
#define FONT_SPRITE_5X8_WIDTH                       5u
#define FONT_SPRITE_5X8_HEIGHT                      8u
#define FONT_SPRITE_5X8_FONT_TABLE_SIZE             102u


/******************************************************************************************************************************************************
 *  G L O B A L   F U N C T I O N   M A C R O S
******************************************************************************************************************************************************/


/******************************************************************************************************************************************************
 *  C L A S S   F O N T S P R I T E 5 X 8
******************************************************************************************************************************************************/
class FontSprite5x8 : public Font<FontCharVertical<byte, FONT_SPRITE_5X8_WIDTH>, FONT_SPRITE_5X8_FONT_TABLE_SIZE>
{
/******************************************************************************************************************************************************
 *  P U B L I C   D A T A   T Y P E S   A N D   S T R U C T U R E S
******************************************************************************************************************************************************/
  public:
    using FontCharType = FontCharVertical<byte, FONT_SPRITE_5X8_WIDTH>;
    using ColumnsType = std::array<byte, FONT_SPRITE_5X8_WIDTH>;

/******************************************************************************************************************************************************
 *  P R I V A T E   D A T A   A N D   F U N C T I O N S
******************************************************************************************************************************************************/
  private:
    static const FontTableType FontTable;
  
/******************************************************************************************************************************************************
 *  P U B L I C   F U N C T I O N S
******************************************************************************************************************************************************/
  public:
    constexpr FontSprite5x8() : Font(FontTable) { }
    ~FontSprite5x8() { }

    // get methods
    byte getWidth() const { return FONT_SPRITE_5X8_WIDTH; }
    byte getHeight() const { return FONT_SPRITE_5X8_HEIGHT; }
    Orientation getOrientation() const { return Orientation::ORIENTATION_VERTICAL; }
    const FontTableType& getFontTable() const { return FontTable; }
        
    // set methods

    // methods

};


