/* Scheduler configuration parameter */
#define SCHEDULER_TASK_CYCLE                100u

/* Scheduler parameter */



/******************************************************************************************************************************************************
 *  G L O B A L   F U N C T I O N   M A C R O S
******************************************************************************************************************************************************/


/******************************************************************************************************************************************************
 *  C L A S S   S C H E D U L E R
******************************************************************************************************************************************************/
class Scheduler
{
/******************************************************************************************************************************************************
 *  P U B L I C   D A T A   T Y P E S   A N D   S T R U C T U R E S
******************************************************************************************************************************************************/
  public:

/******************************************************************************************************************************************************
 *  P R I V A T E   D A T A   A N D   F U N C T I O N S
******************************************************************************************************************************************************/
  private:
    static constexpr byte TaskCycle{SCHEDULER_TASK_CYCLE};
    byte TaskCycleCounter{0u};

    // functions
    bool isCycleHit(byte);
    void triggerTasks();

/******************************************************************************************************************************************************
 *  P U B L I C   F U N C T I O N S
******************************************************************************************************************************************************/
  public:
    constexpr Scheduler() { }
    ~Scheduler() { }

	// get methods
    static constexpr byte getTaskCycle() { return TaskCycle; }
    byte getTaskCycleCounter() const { return TaskCycleCounter; }

	// set methods
    void setTaskCycleCounter(byte sTaskCycleCounter) { TaskCycleCounter = sTaskCycleCounter; }

	// methods
    void task();
    static byte convertSpeedToTaskCycle(byte Speed) {
        if(Speed == UINT8_MAX) { return 1u; }
        if(Speed == 0u) { return 0u; }
        return UINT8_MAX - Speed;
    }
    static byte convertTaskCycleToSpeed(byte TaskCylce) {
        if(TaskCycle == UINT8_MAX) { return 1u; }
        if(TaskCycle == 0u) { return 0u; }
        return UINT8_MAX - (TaskCylce - 1u);
    }
};

#endif

